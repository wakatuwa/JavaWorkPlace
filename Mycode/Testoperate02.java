public class Testoperate02 {
	public static void main(String[] args) {
		int a = 3;
		System.out.println(a==3);
		System.out.println(a!=3);
		System.out.println(a<5);

		char b = 'a';
		char b2 = 'c';
		
		
		System.out.println((int)b);
		System.out.println(0 + b2);
		System.out.println(b<b2);
		
	}
}
